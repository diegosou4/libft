int	ft_isdigit(char *str)
{
	int	i;
	int	j;

	j = 0;
	if (str[0] == '\0')
	{
		i = 1;
	}
	while (str[j] != '\0')
	{
		if (str[j] >= 48 && str[j] <= 57)
		{
			i = 1;
		}
		else
		{
			return (0);
		}
		j++;
	}
	return (i);
}
