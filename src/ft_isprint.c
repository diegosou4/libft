int	ft_is_print(char *str)
{
	int	i;
	int	j;

	j = 0;
	i = 0;
	if (str[0] == '\0')
	{
		return (1);
	}
	while (str[j] != '\0')
	{
		if (str[j] > 31 && str[j] < 127)
		{
			i = 1;
		}
		else
		{
			return (0);
		}
		j++;
	}
	return (i);
}
